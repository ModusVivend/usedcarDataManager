def get_series(brand_key: str) -> dict:
    """获取某品牌的所有车系"""
    return SERIES_DB.get(brand_key, {})


def get_series_names(brand_key: str) -> list[str]:
    """获取某品牌的车系名称列表"""
    return list(SERIES_DB.get(brand_key, {}).keys())


def get_series_info(brand_key: str, series_name: str) -> dict | None:
    """获取某车系详细信息"""
    brand_series = SERIES_DB.get(brand_key, {})
    return brand_series.get(series_name)


def get_trims(brand_key: str, series_name: str) -> list[str]:
    """获取某车系的所有配置款"""
    info = get_series_info(brand_key, series_name)
    return info["trims"] if info else []


def get_price_range(brand_key: str, series_name: str) -> tuple | None:
    """获取新车指导价区间"""
    info = get_series_info(brand_key, series_name)
    return info["price_range"] if info else None


def generate_years(brand_key: str, series_name: str) -> list[int]:
    """生成某车系可选的上牌年份列表"""
    info = get_series_info(brand_key, series_name)
    if not info:
        return list(range(2010, 2026))
    start, end = info["years"]
    end_year = 2026 if end == "至今" else end
    return list(range(max(start, 2005), end_year + 1))
